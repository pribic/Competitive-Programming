public class A {
  static FastScanner sc = new FastScanner(System.in);

  public static void main(String[] args) {
    try (PrintWriter out = new PrintWriter(System.out)) {
      int T = sc.nextInt();
      for (int tt = 1; tt <= T; tt++) {
        int n = sc.nextInt();
        int[] arr = new int[n];
        for (int i = 0; i < n; i++) {
          arr[i] = sc.nextInt();
        }
        int[] arr1 = new int[n];
        for (int i = 0; i < n; i++) {
          arr1[i] = sc.nextInt();
        }
        int max1 = IntStream.of(arr).max().getAsInt();
        int max2 = IntStream.of(arr1).max().getAsInt();
        if (max1 >= max2) {
          for (int i = 0; i < n; i++) {
            //move smaller element to arr1
            if (arr[i] < arr1[i])
              arr1[i] = arr[i];
          }
          System.out.println((long) max1 * IntStream.of(arr1).max().getAsInt());
        } else {
          for (int i = 0; i < n; i++) {
            //move smaller element to arr1
            if (arr1[i] < arr[i])
              arr[i] = arr1[i];
          }
          System.out.println((long) max2 * IntStream.of(arr).max().getAsInt());
        }
      }
    }
  }

  static class FastScanner {
    BufferedReader br;
    StringTokenizer st;

    public FastScanner(File f) {
      try {
        br = new BufferedReader(new FileReader(f));
      } catch (FileNotFoundException e) {
        e.printStackTrace();
      }
    }

    public FastScanner(InputStream f) {
      br = new BufferedReader(new InputStreamReader(f), 32768);
    }

    String next() {
      while (st == null || !st.hasMoreTokens()) {
        String s = null;
        try {
          s = br.readLine();
        } catch (IOException e) {
          e.printStackTrace();
        }
        if (s == null)
          return null;
        st = new StringTokenizer(s);
      }
      return st.nextToken();
    }

    boolean hasMoreTokens() {
      while (st == null || !st.hasMoreTokens()) {
        String s = null;
        try {
          s = br.readLine();
        } catch (IOException e) {
          e.printStackTrace();
        }
        if (s == null)
          return false;
        st = new StringTokenizer(s);
      }
      return true;
    }

    int nextInt() {
      return Integer.parseInt(next());
    }

    long nextLong() {
      return Long.parseLong(next());
    }

    double nextDouble() {
      return Double.parseDouble(next());
    }
  }
}